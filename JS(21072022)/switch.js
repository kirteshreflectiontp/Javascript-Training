function cars(id){
    switch(id){
        case'newcar':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;
        case'usedcar':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;
        case'sellcar':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;
        case'new&review':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;
        case'ventures':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;
        case'more':
        if(document.getElementById(id).style.display =='none'){
            document.getElementById(id).style.display = 'block';
        }
        else{
            document.getElementById(id).style.display = 'none';
        }
        break;


    }
}